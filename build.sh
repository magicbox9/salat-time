download_adhan_assabile "adhan_kourdi.mp3"        "8c052a5edec1"  # Ahmed Al-Kourdi
download_adhan_assabile "adhan_feqy.mp3"          "83e17f5db5a7"  # Mohamed Abdel-Basset Al-Feqy

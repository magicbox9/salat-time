# Legacy filenames — any older install whose UserDefaults still references
# the old catalog entries ("adhan.mp3", "adhan_makkah.mp3", etc.) gets
# redirected to the new Makkah recording so the app never fails to load
# a sound on first launch after upgrading.
if [ -f "$RESOURCES_DIR/adhan_makkah_mulla.mp3" ]; then
    [ ! -f "$RESOURCES_DIR/adhan.mp3" ]         && cp "$RESOURCES_DIR/adhan_makkah_mulla.mp3" "$RESOURCES_DIR/adhan.mp3"
    [ ! -f "$RESOURCES_DIR/adhan_makkah.mp3" ]  && cp "$RESOURCES_DIR/adhan_makkah_mulla.mp3" "$RESOURCES_DIR/adhan_makkah.mp3"
    [ ! -f "$RESOURCES_DIR/adhan_madina.mp3" ]  && cp "$RESOURCES_DIR/adhan_makkah_mulla.mp3" "$RESOURCES_DIR/adhan_madina.mp3"
fi

# ---------- Cairo fonts (Arabic UI) ----------------------------------------
echo "Fetching Cairo fonts..."
download_font () {
    local fname="$1"
    local url="$2"
    local out="$RESOURCES_DIR/$fname"
    if curl -sSfL --max-time 30 -o "$out" "$url"; then
        local size=$(stat -f%z "$out" 2>/dev/null || echo 0)
        if [ "$size" -gt 10000 ]; then
            echo "  $fname  ($size bytes)"
            return 0
        fi
    fi
    rm -f "$out"
    return 1
}

# Google Fonts removed the /static per-weight files in 2024 — only a single
# variable font remains at the top of the Cairo folder. macOS + Swift can use
# a variable font just fine; we register it once and ask NSFontManager for the
# weight we want at render time (see `Localizer.font(size:weight:)`).
# The filename contains "[slnt,wght]" which needs URL-encoding in the path.
download_font "Cairo-Variable.ttf" \
    "https://raw.githubusercontent.com/google/fonts/main/ofl/cairo/Cairo%5Bslnt%2Cwght%5D.ttf" \
    || echo "  (Cairo-Variable skipped)"

